using System;

public static class GestureNumberHelper
{
	public static string ToString(int gestureNumber) {
		switch (gestureNumber) {
		case (int) GestureNumber.NOTHING_HAS_DETECTED:
			return "Nothing has detected.";
		case (int) GestureNumber.THUMB_INDEX_FINGER:
			return "Thumb + index finger";
		case (int) GestureNumber.THUMB_MIDDLE_FINGER:
			return "Thumb + middle finger";
		case (int) GestureNumber.THUMB_PINKY:
			return "Thumb + pinky finger";
		case (int) GestureNumber.THUMB_RING_FINGER:
			return "Thumb + ring finger";
		}

		return "Nothing has detected.";
	}
}